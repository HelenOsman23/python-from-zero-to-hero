# Hacer un programa que determine el perimetro de una torta en centimetros
# Para el valor de pi, utilizar 3,14
# El usuario del programa ingresará mediante el teclado el radio de la torta

# Solicitar al usuario el radio de la torta en centímetros
ceke_radius = int(input("Indicar el rádio de la torta en centimetros: "))

# Valor de pi
pi = 3.14

# Calcular el perímetro de la torta
perimeter = 2 * pi * ceke_radius

# Mostrar el resultado
print("El perímetro de la torta es de:",perimeter, "centímetros.")



